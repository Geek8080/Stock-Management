class MyCustomFilter extends javax.swing.filechooser.FileFilter {

    @Override
    public boolean accept(File file) {
        // Allow only directories, or files with ".txt" extension
        return file.isDirectory() || file.getAbsolutePath().endsWith(".jpg") || file.getAbsolutePath().endsWith(".JPG") || file.getAbsolutePath().endsWith(".jpeg") || file.getAbsolutePath().endsWith(".JPEG") || file.getAbsolutePath().endsWith(".png") || file.getAbsolutePath().endsWith(".gif") || file.getAbsolutePath().endsWith(".ico");
    }

    @Override
    public String getDescription() {
        // This description will be displayed in the dialog,
        // hard-coded = ugly, should be done via I18N
        return "Images (*.jpeg or *.jpg or *.png or *.gif or *.ico)";
    }
}

