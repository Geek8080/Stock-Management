    private void jLabel12MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel12MouseClicked
        if (!(id.getText().equals("") || id.getText() == null)) {
            int returnVal = fileChooser.showOpenDialog(this);
            image = "Data/" + id.getText() + "/Icon.jpg";
            File dst = new File("Data/" + id.getText().trim());
            dst.mkdirs();
            dst = new File(image);
            if (returnVal == JFileChooser.APPROVE_OPTION) {
                try {
                    File file = fileChooser.getSelectedFile();
                    dir.resImage(file, dst.getAbsolutePath());
                    jLabel10.setBackground(Color.WHITE);
                    jLabel10.setIcon(new ImageIcon(image));
                    Files.copy(file.toPath(), new File(image.replace("/Icon.jpg", "/Pic.jpg")).toPath());
                } catch (IOException ex) {
                    Logger.getLogger(addItem.class.getName()).log(Level.SEVERE, null, ex);
                    dir.writeFile("errLog", "Error copying the file...\n");
                }
            } else {
                dir.writeFile("errLog", "File access cancelled by user.\n");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Enter the details first...");
        }
    }//GEN-LAST:event_jLabel12MouseClicked

