    private void jLabel14MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel14MouseClicked
        String PID, Pname, Titems, Pprice, des;
        PID = id.getText().trim();
        Pname = name.getText().trim();
        Titems = items.getText().trim();
        Pprice = mrp.getText().trim();
        des = jTextArea1.getText();
        int tworth = Integer.parseInt(Titems) * Integer.parseInt(Pprice);
        DateFormat df = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
        java.util.Date today = java.util.Calendar.getInstance().getTime();
        String date = df.format(today);

        if (!(new File("Data/" + PID).exists())) {
            new File("Data/" + PID).mkdirs();
        }
        dir.writeFile(PID + "/Description", des);
        des = "Data/" + PID + "/Description";

        String Query1, Query2;
        Query1 = "INSERT INTO Products(PID,Name,Items,Price,TotalWorth,LastUpdated,Description,Image) VALUES('" + PID + "','" + Pname + "'," + Titems + "," + Pprice + "," + tworth + ",'" + date + "','" + des + "','" + image + "')";
        PID = PID.trim().replaceAll(" ", "");
        Query2 = "CREATE TABLE `" + PID + "`( Items INT, Price INT, Total Int, Date Varchar(25))";
        query.runCUD(Query1);
        query.runCUD(Query2);
        System.out.println(PID + " has been created...");
        JOptionPane.showMessageDialog(this, "Item added to the stock...");
        new addItem().setVisible(true);
        this.setVisible(false);

    }//GEN-LAST:event_jLabel14MouseClicked

